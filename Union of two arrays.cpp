class Solution{
    public:
    //Function to return the count of number of elements in union of two arrays.
    int doUnion(int a[], int n, int b[], int m)  {
        //code here
           int arr[n+m];

        for(int i=0;i<n;i++){

            arr[i]=a[i];

        }

        for(int i=0;i<m;i++){

            arr[i+n]=b[i];

        }

        sort(arr,arr+(n+m));

        int count =0;

        for(int i=0;i<(n+m-1);i++){

            if(arr[i]==arr[i+1]){

                count++;

            }

        }

        return n+m-count;
    }
};

