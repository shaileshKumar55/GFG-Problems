class Solution
{
    public:
    //Function to swap odd and even bits.
    unsigned int swapBits(unsigned int n)
    {
    	// Your code here
    	int out=0;
    	for(int i=0;i<=31;i+=2){
    	    
    	   if(n & 1) out+=pow(2,i+1);
    	   if((n>>1)&1)out+=pow(2,i);
    	   n=n>>2;
    	}
    	return out;
    }
};

