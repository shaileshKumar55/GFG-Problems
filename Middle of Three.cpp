class Solution{
  public:
    int middle(int A, int B, int C){
        //code here//Position this line where user code will be pasted.
        int maxx = max(A, max(B, C));
        int minn = min(A, min(B, C));
        
        return (A + B + C) - (maxx + minn);
    }
};

