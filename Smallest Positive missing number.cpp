class Solution
{
    public:
    //Function to find the smallest positive number missing from the array.
    int missingNumber(int arr[], int n) 
    { 
        // Your code here
        unordered_set<int> s;
        int maxi = arr[0];
        for(int i=0;i<n;i++)
        {
            if(arr[i] >= 0)
            {
                s.insert(arr[i]);
            }
            maxi = max(maxi,arr[i]);
        }
        for(int i=1;i<=(maxi+1);i++)
        {
            if(s.find(i) == s.end())
            {
                return i;
            }
        }
        return 1;
    } 
};

